<?php

namespace Squareetlabs\LaravelTeamsPermissions\Tests;

use Illuminate\Support\Facades\Schema;
use Orchestra\Testbench\TestCase as Orchestra;
use Squareetlabs\LaravelTeamsPermissions\TeamsServiceProvider;
use Squareetlabs\LaravelTeamsPermissions\Tests\Models\User;

class TestCase extends Orchestra
{
    protected function setUp(): void
    {
        parent::setUp();

        $this->setUpDatabase();
    }

    protected function getPackageProviders($app)
    {
        return [
            TeamsServiceProvider::class,
        ];
    }

    public function getEnvironmentSetUp($app)
    {
        config()->set('database.default', 'testing');
        
        // Configure User model for tests
        config()->set('teams.models.user', User::class);
    }

    /**
     * Set up the database for testing.
     *
     * @return void
     */
    protected function setUpDatabase(): void
    {
        // Create users table if it doesn't exist
        if (!Schema::hasTable('users')) {
            Schema::create('users', function ($table) {
                $table->id();
                $table->string('name');
                $table->string('email')->unique();
                $table->timestamp('email_verified_at')->nullable();
                $table->string('password');
                $table->rememberToken();
                $table->timestamps();
            });
        }

        // Run package migrations
        $migrationPath = __DIR__ . '/../database/migrations';
        
        // Load migrations manually
        $migrations = [
            'create_teams_table.php',
            'create_permissions_table.php',
            'create_roles_table.php',
            'create_team_user_table.php',
            'create_abilities_table.php',
            'create_entity_ability_table.php',
            'create_groups_table.php',
            'create_group_user_table.php',
            'create_entity_permission_table.php',
        ];

        if (config('teams.invitations.enabled')) {
            $migrations[] = 'create_invitations_table.php';
        }

        if (config('teams.audit.enabled')) {
            $migrations[] = 'create_team_audit_logs_table.php';
        }

        $migrations[] = 'add_performance_indexes.php';

        foreach ($migrations as $migration) {
            $migrationFile = $migrationPath . '/' . $migration;
            if (file_exists($migrationFile)) {
                $migrationClass = include $migrationFile;
                if ($migrationClass instanceof \Illuminate\Database\Migrations\Migration) {
                    $migrationClass->up();
                }
            }
        }
    }
}
